#!/usr/bin/env ruby

require_relative "../lib/github_collaborators"

puts "Start"

check_github = true
github_outside_collaborators = GithubCollaborators::OutsideCollaborators.new(check_github)
github_outside_collaborators.is_renewal_within_one_month
github_outside_collaborators.remove_unknown_collaborators
