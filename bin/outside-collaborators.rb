github_outside_collaborators.has_review_date_expired

check_github = false
terraform_files_outside_collaborators = GithubCollaborators::OutsideCollaborators.new(check_github)
terraform_files_outside_collaborators.is_renewal_within_one_month
terraform_files_outside_collaborators.remove_unknown_collaborators
terraform_files_outside_collaborators.is_review_date_within_a_week
terraform_files_outside_collaborators.has_review_date_expired

